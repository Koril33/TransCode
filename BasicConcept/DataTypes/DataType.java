class DataType {
    public static void main(String[] args) {
        byte b = 1;
        short s = 2;
        int n = 3;
        long l = 4L;
        float f = 3.14f;
        double d = 1.23;
        char c = 'a';
        boolean bool = true;

        System.out.println(
            "byte: "      + b +
            "\nshort: "   + s +
            "\nint: "     + n +
            "\nlong: "    + l +
            "\nfloat: "   + f +
            "\ndouble: "  + d +
            "\nchar: "    + c +
            "\nboolean: " + bool
        );
    }
}
